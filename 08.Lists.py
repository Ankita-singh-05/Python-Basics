
places = ["Pune", "Satara", "Nashik", "Nagpur"]

# To print the whole list
print(places)

# To access any of the elements in the list
print(places[0])

# to access all the elements after the index 1
print(places[1:])

print(places[0:2])

# to print the last element
print(places[-1])

# Modifying the list
places[1] = "Sangli"
print(places)


