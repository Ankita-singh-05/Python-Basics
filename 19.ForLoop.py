# For loop allows us to loop different collections


for letter in "Giraffe Academy":
    print(letter)

friends = ["Geeta", "Shivani", "Kalyani"]
for index in range(len(friends)):
    print(friends[index])

for name in range(0, 3):
    print(name)

for i in range(5):
    if i == 0:
        print("first iteration")
    else:
        print("Not first")
